package com.citius.jasper;

public class PhysicianDto {
	
	private String physicianName;
	private String qualification;
	private int regNo;
	private String clinicNameAddress;
	public String getPhysicianName() {
		return physicianName;
	}
	public void setPhysicianName(String physicianName) {
		this.physicianName = physicianName;
	}
	public String getQualification() {
		return qualification;
	}
	public void setQualification(String qualification) {
		this.qualification = qualification;
	}
	public int getRegNo() {
		return regNo;
	}
	public void setRegNo(int regNo) {
		this.regNo = regNo;
	}
	public String getClinicNameAddress() {
		return clinicNameAddress;
	}
	public void setClinicNameAddress(String clinicNameAddress) {
		this.clinicNameAddress = clinicNameAddress;
	}
	
	
	

}
